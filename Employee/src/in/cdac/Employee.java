package in.cdac;

public class Employee {
	private int id;
	private String name;
	private int salary;
	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getsalary() {
		return salary;
	}

	public void setsalary(int salary) {
		this.salary = salary;
	}

	public int getage() {
		return age;
	}

	public void setage(int age) {
		this.age = age;
	}

}
